import express from "express";
import fs from "fs";
import path from "path";
import multer from "multer";
import dotenv from "dotenv";
import { google } from "googleapis";

dotenv.config();

const app = express();
app.use(express.json());

// ===============================
// ENV + PORT
// ===============================
const PORT = process.env.PORT || 10000;
const CLIENT_ID = process.env.YOUTUBE_CLIENT_ID;
const CLIENT_SECRET = process.env.YOUTUBE_CLIENT_SECRET;
const REDIRECT_URI = process.env.YOUTUBE_REDIRECT_URI;

// ===============================
// UPLOADS DIR
// ===============================
const UPLOAD_DIR = path.join(process.cwd(), "uploads");
if (!fs.existsSync(UPLOAD_DIR)) {
  fs.mkdirSync(UPLOAD_DIR);
}

// ===============================
// MULTER CONFIG
// ===============================
const upload = multer({ dest: UPLOAD_DIR });

// ===============================
// GOOGLE OAUTH CLIENT
// ===============================
const oauth2Client = new google.auth.OAuth2(
  CLIENT_ID,
  CLIENT_SECRET,
  REDIRECT_URI
);

// ===============================
// TOKEN FILE (TEMP ONLY)
// ===============================
const TOKEN_PATH = path.join(process.cwd(), "tokens.json");

// ===============================
// AUTH ROUTE
// ===============================
app.get("/auth", (req, res) => {
  const authUrl = oauth2Client.generateAuthUrl({
    access_type: "offline",
    prompt: "consent",
    scope: [
      "https://www.googleapis.com/auth/youtube.upload",
      "https://www.googleapis.com/auth/youtube.force-ssl",
    ],
  });
  res.redirect(authUrl);
});

// ===============================
// AUTH CALLBACK
// ===============================
app.get("/auth/callback", async (req, res) => {
  try {
    const { code } = req.query;
    const { tokens } = await oauth2Client.getToken(code);

    // ðŸ”¥ IMPORTANT: REFRESH TOKEN LOG
    console.log("REFRESH_TOKEN =>", tokens.refresh_token);

    oauth2Client.setCredentials(tokens);

    // TEMP: write tokens to file
    fs.writeFileSync(TOKEN_PATH, JSON.stringify(tokens, null, 2));

    res.send("âœ… YouTube Connected Successfully. You can close this tab.");
  } catch (err) {
    console.error("OAuth Error:", err);
    res.status(500).send("Auth error: " + err.message);
  }
});

// ===============================
// YOUTUBE CLIENT
// ===============================
const youtube = google.youtube({
  version: "v3",
  auth: oauth2Client,
});

// ===============================
// UPLOAD ROUTE (MANUAL)
// ===============================
app.post("/upload", upload.single("video"), async (req, res) => {
  try {
    const { title, description } = req.body;

    const response = await youtube.videos.insert({
      part: "snippet,status",
      requestBody: {
        snippet: {
          title: title || "AIVANA Upload",
          description: description || "Uploaded via AIVANA Backend",
          categoryId: "28",
        },
        status: {
          privacyStatus: "public",
        },
      },
      media: {
        body: fs.createReadStream(req.file.path),
      },
    });

    res.json({
      success: true,
      videoId: response.data.id,
      url: `https://www.youtube.com/watch?v=${response.data.id}`,
    });
  } catch (err) {
    console.error(err);
    res.status(500).json({ error: err.message });
  }
});

// ===============================
// GENERATE + UPLOAD (AUTO PIPELINE)
// ===============================
app.post("/generate-and-upload", async (req, res) => {
  try {
    const autoVideoPath = path.join(UPLOAD_DIR, "aivana-auto.txt");
    fs.writeFileSync(autoVideoPath, "AIVANA AUTO CONTENT");

    const response = await youtube.videos.insert({
      part: "snippet,status",
      requestBody: {
        snippet: {
          title: "AIVANA Auto Upload",
          description: "Auto-generated by AIVANA Universe",
          categoryId: "28",
        },
        status: {
          privacyStatus: "public",
        },
      },
      media: {
        body: fs.createReadStream(autoVideoPath),
      },
    });

    res.json({
      success: true,
      videoId: response.data.id,
      url: `https://www.youtube.com/watch?v=${response.data.id}`,
    });
  } catch (err) {
    console.error(err);
    res.status(500).json({ error: err.message });
  }
});

// ===============================
// HEALTH CHECK
// ===============================
app.get("/", (req, res) => {
  res.send("ðŸš€ AIVANA YouTube Backend is running");
});

// ===============================
// START SERVER
// ===============================
app.listen(PORT, () => {
  console.log(`ðŸš€ Backend running on port ${PORT}`);
});
